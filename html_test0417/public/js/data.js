function findStockCode(input) {
  input = input.trim().toLowerCase();
  if (/^(sh|sz|bj)\d{6}$/.test(input)) return input;
  if (/^\d{6}$/.test(input)) {
    if (input.startsWith('6') || input.startsWith('68')) return 'sh' + input;
    if (input.startsWith('0') || input.startsWith('3') || input.startsWith('2')) return 'sz' + input;
    return input;
  }
  if (nameToCode[input]) return nameToCode[input];
  // fuzzy
  for (const [name, code] of Object.entries(nameToCode)) {
    if (name.includes(input) || input.includes(name)) return code;
  }
  return null;
}

